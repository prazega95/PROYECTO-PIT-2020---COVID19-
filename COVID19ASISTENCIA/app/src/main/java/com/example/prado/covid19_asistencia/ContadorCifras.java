package com.example.prado.covid19_asistencia;

public class ContadorCifras {

    private String indicadorConfirmados;
    private String indicadorHospitalizado;
    private String indicadorMuertes;
    private String indicadorRecuperado;



    public String getIndicadorConfirmados() {
        return indicadorConfirmados;
    }

    public void setIndicadorConfirmados(String indicadorConfirmados) {
        this.indicadorConfirmados = indicadorConfirmados;
    }

    public String getIndicadorHospitalizado() {
        return indicadorHospitalizado;
    }

    public void setIndicadorHospitalizado(String indicadorHospitalizado) {
        this.indicadorHospitalizado = indicadorHospitalizado;
    }

    public String getIndicadorMuertes() {
        return indicadorMuertes;
    }

    public void setIndicadorMuertes(String indicadorMuertes) {
        this.indicadorMuertes = indicadorMuertes;
    }

    public String getIndicadorRecuperado() {
        return indicadorRecuperado;
    }

    public void setIndicadorRecuperado(String indicadorRecuperado) {
        this.indicadorRecuperado = indicadorRecuperado;
    }
}
